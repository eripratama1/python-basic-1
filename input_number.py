# Contoh input number

print("Angka pertama :")
angkaPertama = int(input())

print("Angka kedua")
angkaKedua = int(input())

hasilTambah = angkaPertama + angkaKedua
hasilKurang = angkaPertama - angkaKedua

print(f"{angkaPertama} + {angkaKedua} = {hasilTambah}")
print(f"{angkaPertama} - {angkaKedua} = {hasilKurang}")
